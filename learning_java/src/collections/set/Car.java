package collections.set;

public class Car {
	private String color;
	private String mod;
	private Integer age;

	public Car(String color, String mod, Integer age) {
		super();
		this.color = color;
		this.mod = mod;
		this.age = age;
	}

	public String getColor() {
		return color;
	}

	public String getMod() {
		return mod;
	}

	public int getAge() {
		return age;
	}

}
