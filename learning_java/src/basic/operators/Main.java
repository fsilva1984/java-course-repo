package basic.operators;

public class Main {

	public static void main(String[] args) {

		// Operadores Unario

		System.out.println("\n Operadores Unarios\n");

		System.out.println("++i, Incremento pré­fixado");
		System.out.println("--i, Decremento pré­fixado");

		System.out.println("i++, Incremento pós­fixado");
		System.out.println("i--, Decremento pós­fixado");

		System.out.println("!, Negação lógica");

		// Operadores Binario

		System.out.println("\n Operadore Binarios\n");
		System.out.println("+ Adicao");
		System.out.println("- Subtracao");
		System.out.println("* Multiplicacao");
		System.out.println("/ Divisao");
		System.out.println("% Modulo");

		System.out.println("= Atribuicao");
		System.out.println("+= Atribuicao Aditiva");
		System.out.println("-= Atribuicao Substantiva");
		System.out.println("/= Atribuicao de Divisao");
		System.out.println("%= Atribuicao de Modulo");
		System.out.println("*= Atribuicao de Multiplicacao");

		// Operadores Binarios Logicos
		System.out.println("\n Operadores binarios Logicos\n");
		System.out.println("< Menor que");
		System.out.println("> Maior que");
		System.out.println("<= Menor que ou igual");
		System.out.println(">= Maior que ou igual");
		System.out.println("== Igualdade");
		System.out.println("!= Desigualdade");
		System.out.println("&& E lógico");
		System.out.println("|| UO lógico");

		// Operador ternario
		System.out.println("\n Operador Ternario");

		System.out.println("\n?:");
	}

}
